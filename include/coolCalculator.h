#pragma once

/**
 * @brief This function calculates the addition of two natural numbers.
 *
 * @param a Fist number to add.
 * @param b Second number to add.
 */
int add(int a, int b);

/**
 * @brief This function calculates the subtraction of two natural numbers.
 *
 * @param a Fist number to subtract.
 * @param b Second number to subtract.
 */
int subtract(int a, int b);

/**
 * @brief This function calculates the multiplication of two natural numbers.
 *
 * @param a Fist number to multiply.
 * @param b Second number to multiply.
 */
int multiply(int a, int b);

/**
 * @brief This function calculates the division of two natural numbers.
 *
 * @param a Fist number to divide.
 * @param b Second number to divide.
 */
float divide(int a, int b);
